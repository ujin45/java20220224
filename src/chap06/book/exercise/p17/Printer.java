package chap06.book.exercise.p17;

public class Printer {


	public static void printerln(int i) {
		System.out.println(i);
	}

	public static void printerln(boolean b) {
		System.out.println(b);
		
	}

	public static void printerln(double d) {
		System.out.println(d);
		
	}

	public static void printerln(String string) {
		System.out.println(string);
		
	}
	
	
	
	
}
