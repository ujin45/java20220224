package chap06.lecture.p09getter_setter;

public class Desk {
	private String model;
	private int height;
	private int weight;
	private int price;
	
	public String getModel() {
		return model;
	}
	public int getHeight() {
		return height;
	}
	public int getWeight() {
		return weight;
	}
	public int getPrice() {
		return price;
	}
	
	// source -> Generate getter and setter 에서 자동으로 세팅 가능 
	
}
